<p class="top15">SDC will provide students an opportunity to interact with fellow students, industry and academic experts, 
	share their ideas, experiences and get extensive feedback.</p>

<p>The projects will be reviewed by a two experts and two peer students based on its novelty, relevance to the conference, 
	its ability to engage participants and overall quality. Confidentiality of submissions are maintained during the review process.</p>

<p>Selected best projects will be showcased during the conference. Authors will be given 10 minutes to present, followed 
	by Q & A session for additional 5 minutes. Authors are encouraged to demonstrate their work by presenting a working 
	prototype at conference venue.</p>

<h2 class="top15">Eligibility Criteria</h2>
<ul class="innerUl">
	<li>
		<span class="bullet">•</span> You are under graduate, graduate or PhD student pursuing an academic degree at the 
		time of submission. Students who completed their studies in calendar 2012 are also invited to submit their academic projects.
	</li>
	<li>
		<span class="bullet">•</span> A group can consist of not more than 4 members, from same or different institutes.
	</li>	
</ul>

<h2 class="top15">Submission Format</h2>
<ul class="innerUl">
	<li>
		<span class="bullet">•</span> 
		A maximum of 3 page entry must be submitted, focusing on abstract and summary of solution 
		in IndiaHCI 2013 SDC format. Abstract submitted for review must be uploaded in English language in .PDF format.
	</li>
	<li>
		<span class="bullet">•</span> 
		An optional video file (maximum of 5 minutes) describing your project. Videos must be submitted in .MP4 format 
		(not larger than 20MB file size)</li>
	<li>
		<span class="bullet">•</span> Submissions must be done through easy chair conference management system. To submit a paper
		<ul class="innerUl font14" >
			<li>→ Create an account on <a href="https://www.easychair.org/account/signup.cgi" target="_blank">Easy Chair</a></li>
			<li>→ Come back to this page and <a href="https://www.easychair.org/conferences/?conf=ihci2013" target="_blank">click here</a></li>
			<li>→ Log in with your easychair ID and password</li>
			<li>→ Click on "New submission" menu and fill in your project details</li>
		</ul>
	</li>
</ul>

<h2 class="top15">Rules</h2>
<ul class="innerUl">
	<li><span class="bullet">•</span> Respective guides can be acknowledged separately but should not be listed as authors</li>
	<li><span class="bullet">•</span> Submissions must not make reference to author(s)' name, respective guide/faculty member(s) and practitioner(s)' name and name of the institution(s)</li>
	<li><span class="bullet">•</span> In the initial entry, authors are requested to use discretion in using names or pictures of locations (e.g. Amingaon, Powai etc.). These discretion can be removed once it is accepted. Authors are allowed to refer their own previous work for reference, but similar care must be taken</li>
	<li><span class="bullet">•</span> Submission must not be previously published in any conference or journal and authors must own all copyrights of all materials</li>
	<li><span class="bullet">•</span> Submissions not meeting the format requirements will be automatically disqualified</li>
</ul>


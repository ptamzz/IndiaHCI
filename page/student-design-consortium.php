<p>Student Design Consortium (SDC) gives opportunities to students to demonstrate innovative interactions 
	and interface design & technologies. SDC will also allow attendees to experience new development in interaction 
	design research and academic projects, engage and discuss work with students.</p>

<p>We invite best interaction design projects done by students from all over world. Submissions are invited for original
 work from current and past students. Projects can be from thesis work, short term design projects and design experiments. 
 Students can present their work in below areas (but not limited to):</p>

<ul class="innerUl">
	<li class="col2 left">• human computer interaction</li>
	<li class="col2 left">• gestural user interfaces</li>
	<li class="col2 left">• tangible user interfaces</li>
	<li class="col2 left">• web/mobile/software user interfaces</li>
	<li class="col2 left">• speech user interfaces</li>
	<li class="col2 left">• multimodal interfaces</li>
	<li class="col2 left">• internet of things</li>
	<li class="col2 left">• smart objects</li>
	<li class="col2 left">• intelligent user interfaces</li>
	<li class="col2 left">• ubiquitous computing</li>
	
	<li class="col2 left">• brain computer interfaces</li>
	<li class="col2 left">• augmented reality</li>
	<li class="col2 left">• context aware applications</li>
	<li class="col2 left">• natural user interfaces</li>
	<li class="col2 left">• touch enabled interfaces</li>
	<li class="col2 left">• usability engineering</li>
	<li class="col2 left">• child computer interface</li>

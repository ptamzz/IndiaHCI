<h2 class="top15">Review Process:</h2>

<p>We follow a double blind review process for India HCI. This means that during and after the
review process, the reviewers do not know who were the author(s) were, and the author(s) do
not know who reviewed their papers. At the time of submission, authors are expected to ensure
that their submissions have been blinded. Please remove individual and institutional identities
(author’s name, the name of university or company) from the title and header areas of the paper.
Changing the colour of the text is not enough. Also, please remove the meta data in the Word /
PDF files. Also remove acknowledgements and names of funding agencies. Authors will get an
opportunity to add back all the above information once the review process is complete when you
submit the camera ready version of your paper. Submissions that are not blinded will be
rejected automatically. However, authors should keep citations to all previous work including their own, so that reviewers can ensure that all previous research has been taken into account.
Authors should cite their own work in the third person, “As described by Dalvi et. al [4], …”.
Further suppression of your identity in the body of the paper (e.g. name of town / country,
participating institutions, photographs of researchers etc.) is left to author’s discretion.</p>

<h2 class="top15">Notification of Acceptance and Camera Ready Paper</h2>
<p>All authors will be notified of acceptance or rejection by the dates mentioned against each category along with the review comments on their submissions. Based on the individual comments, authors will be instructed to submit the Camera Ready version of their submissions on or before the advertised dates against different categories. The accepted submissions must be presented at the conference by one of the authors. The presented works will be published in the joint India HCI 2013 and APCHI2013 conference proceedings and will appear in the ACM digital library.</p>


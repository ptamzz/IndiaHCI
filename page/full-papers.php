<p>Contributions are invited from prospective authors with interests in the indicated conference topics and related areas of application. Original research work may be submitted either as full papers (up to 10 pages) or short papers (up to 6 pages). Contributions are also invited in the category of review papers (up to 10 pages), which could summarise past research, identify current trends and project future possibilities. Review papers could cover either a domain, a group of users or a technology.</p>
<p>All contributions should be of high quality, original and not published elsewhere or submitted for publication during the review period. Submissions must be blinded for peer review (see the anonymisation guidelines below). Every submission must identify the theme which best relates to the contents of the paper. Papers will be peer reviewed by the International Program Committee and may be accepted for presentation.
</p>


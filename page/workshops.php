							<h2 class="subtitle" style="margin-top: 20px;">Description</h2>
						<p>Development of software is a complex process, containing stages such as requirement gathering, testing, and product delivery to the end user. Efforts in improvement of these processes has resulted in various process frameworks such as Six Sigma, ISO 9126:2001, and the Capability Maturity Model Integration (CMMI) being adopted by off-shore vendors. Project management methodologies such as PMP, PRINCE 2 and Agile have been widely adopted to provide a common framework for the management of complexity and various stakeholder interests.</p> 
						<p>The aim of this workshop is to examine the effects of cultural differences on quality and productivity in off-shore software development. The role of cultural differences will be considered in relation to attitudes and approaches to both quality and productivity. Cultural differences will be further separated into those originating in differences inherent to cultures, and those stemming from the development and structure of national software communities. Cultural dimensions will be explored in the adoption of frameworks/standard methodologies in offshore software development projects to distill implementation strategies that produce the best outcomes for both customer and vendor. Maintaining a focus on cultural dimensions, the approaches for implementing these standard methodologies/frameworks will be examined as much as the mix of skills, competencies and attitudes required in the on-shore and off-shoring teams.</p>

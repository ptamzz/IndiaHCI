			researchers interested broadly in the area of Human-Computer Interaction (HCI) and its
			applications in various domains of our life. The conference will consist of keynote lectures by
			eminent researchers in the field, tutorials, workshops and paper presentations on all aspects
			of HCI. We are also very interested in contributions from related fields, such as psychology,
			cognitive science, computer graphics etc. The conference also provides an opportunity to
			local, regional and national industries and service providers to showcase their technologies,
			products and services. The theme for this year’s conference is “HCI for Emerging
